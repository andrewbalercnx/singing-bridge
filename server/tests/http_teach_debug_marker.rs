#[tokio::test]
async fn test_dev_teach_html_carries_debug_marker_student_view() {
    let app = spawn_app().await;
    let cookie = app.signup_teacher("teacher@test.example", "myroom").await;
    // Student view: no cookie
    let (status, headers, body) = app.get_html("/teach/myroom", None).await;
    assert_eq!(status, reqwest::StatusCode::OK);
    assert!(headers.contains_key("content-security-policy"));
    assert_eq!(
        headers.get("content-security-policy").unwrap().to_str().unwrap(),
        EXPECTED_CSP
    );
    assert!(
        body.contains(r#"<meta name="sb-debug""#),
        "dev student view missing sb-debug meta tag"
    );
    assert!(
        !body.contains("<!-- sb:debug -->"),
        "dev student view still has placeholder"
    );

    // Sprint 8: session UI mounts into #session-root (static controls are generated by JS)
    assert!(body.contains(r#"id="session-root""#), "student.html missing #session-root");

    // Sprint 8: session-ui.js and theme.css must be loaded
    assert!(
        body.contains(r#"src="/assets/session-ui.js""#),
        "student.html missing session-ui.js"
    );
    assert!(
        body.contains(r#"href="/assets/theme.css""#),
        "student.html missing theme.css"
    );

    // Sprint 8: no Google Fonts URLs (fonts are self-hosted)
    assert!(
        !body.contains("fonts.googleapis.com"),
        "student.html must not reference Google Fonts"
    );
    assert!(
        !body.contains("fonts.gstatic.com"),
        "student.html must not reference Google Fonts CDN"
    );

    // Sprint 8: mobile viewport meta is present (student is responsive)
    assert!(
        body.contains(r#"name="viewport""#),
        "student.html missing viewport meta tag"
    );

    // Landing-page gating notices (student-only).
    assert!(body.contains(r#"id="block-notice""#), "student.html missing #block-notice");
    assert!(body.contains(r#"id="degraded-notice""#), "student.html missing #degraded-notice");

    // Sprint 4: quality badge + reconnect banner + floor violation notice (still present outside session-root).
    assert!(body.contains(r#"id="quality-badge""#), "student.html missing #quality-badge");
    assert!(body.contains(r#"id="reconnect-banner""#), "student.html missing #reconnect-banner");
    assert!(body.contains(r#"id="floor-violation""#), "student.html missing #floor-violation");

    // Sprint 4: new script tags for adapt/quality/reconnect/session-core loaded in order.
    let adapt_idx = body.find(r#"src="/assets/adapt.js""#).expect("student.html missing adapt.js");
    let quality_idx = body.find(r#"src="/assets/quality.js""#).expect("student.html missing quality.js");
    let reconnect_idx = body.find(r#"src="/assets/reconnect.js""#).expect("student.html missing reconnect.js");
    let sessioncore_idx = body.find(r#"src="/assets/session-core.js""#).expect("student.html missing session-core.js");
    let signalling_idx = body.find(r#"src="/assets/signalling.js""#).expect("student.html missing signalling.js");
    assert!(adapt_idx < quality_idx, "adapt.js must load before quality.js");
    assert!(quality_idx < reconnect_idx, "quality.js must load before reconnect.js");
    assert!(reconnect_idx < sessioncore_idx, "reconnect.js must load before session-core.js");
    assert!(sessioncore_idx < signalling_idx, "session-core.js must load before signalling.js");

    // session-ui.js must load before signalling.js (it provides window.sbSessionUI)
    let session_ui_idx = body.find(r#"src="/assets/session-ui.js""#).expect("student.html missing session-ui.js");
    assert!(session_ui_idx < signalling_idx, "session-ui.js must load before signalling.js");

    // Sprint 9: new script tags present and load order maintained.
    let self_check_idx = body.find(r#"src="/assets/self-check.js""#).expect("student.html missing self-check.js");
    let lobby_toast_idx = body.find(r#"src="/assets/lobby-toast.js""#).expect("student.html missing lobby-toast.js");
    let chat_drawer_idx = body.find(r#"src="/assets/chat-drawer.js""#).expect("student.html missing chat-drawer.js");
    let panels_idx = body.find(r#"src="/assets/session-panels.js""#).expect("student.html missing session-panels.js");
    assert!(chat_drawer_idx < session_ui_idx, "chat-drawer.js must load before session-ui.js");
    assert!(panels_idx < session_ui_idx, "session-panels.js must load before session-ui.js");
    // self-check and lobby-toast just need to be present before session scripts.
    let _ = self_check_idx;
    let _ = lobby_toast_idx;

    // Sprint 9: legacy static elements removed — assert their IDs are absent.
    assert!(
        !body.contains(r#"id="lobby-message-banner""#),
        "student.html must not contain legacy #lobby-message-banner"
    );
    assert!(
        !body.contains(r#"id="chat-panel""#),
        "student.html must not contain legacy #chat-panel"
    );

    drop(cookie);
    app.shutdown().await;
}

#[tokio::test]
async fn test_authenticated_owner_teach_redirects_to_dashboard() {
    let app = spawn_app().await;
    let cookie = app.signup_teacher("teacher@test.example", "myroom").await;
    // Sprint 17: authenticated owner at /teach/<slug> gets 302 → dashboard
    let (status, headers, _body) = app.get_html("/teach/myroom", Some(&cookie)).await;
    assert_eq!(status, reqwest::StatusCode::FOUND);
    assert_eq!(
        headers.get("location").unwrap().to_str().unwrap(),
        "/teach/myroom/dashboard",
        "redirect must point to /teach/<slug>/dashboard"
    );
    assert_eq!(
        headers.get("cache-control").unwrap().to_str().unwrap(),
        "private, no-store",
        "redirect must carry private cache headers"
    );
    assert_eq!(
        headers.get("vary").unwrap().to_str().unwrap(),
        "Cookie",
    );
    app.shutdown().await;
}

#[tokio::test]
async fn test_session_route_serves_teacher_html() {
    let app = spawn_app().await;
    let cookie = app.signup_teacher("teacher@test.example", "myroom").await;
    // Sprint 17: /teach/<slug>/session serves teacher.html for authenticated owner
    let (status, headers, body) = app.get_html("/teach/myroom/session", Some(&cookie)).await;
    assert_eq!(status, reqwest::StatusCode::OK);
    assert!(headers.contains_key("content-security-policy"));
    assert_eq!(
        headers.get("cache-control").unwrap().to_str().unwrap(),
        "private, no-store"
    );
    assert_eq!(headers.get("vary").unwrap().to_str().unwrap(), "Cookie");
    assert!(
        body.contains(r#"<meta name="sb-debug""#),
        "dev session view missing sb-debug meta tag"
    );
    assert!(body.contains(r#"id="session-root""#), "teacher.html missing #session-root");
    assert!(body.contains(r#"src="/assets/session-ui.js""#), "teacher.html missing session-ui.js");
    assert!(body.contains(r#"href="/assets/theme.css""#), "teacher.html missing theme.css");
    assert!(!body.contains("fonts.googleapis.com"), "teacher.html must not reference Google Fonts");
    assert!(!body.contains("fonts.gstatic.com"));

    assert!(body.contains(r#"id="quality-badge""#), "teacher.html missing #quality-badge");
    assert!(body.contains(r#"id="reconnect-banner""#), "teacher.html missing #reconnect-banner");
    assert!(body.contains(r#"id="floor-violation""#), "teacher.html missing #floor-violation");

    let t_adapt = body.find(r#"src="/assets/adapt.js""#).expect("teacher.html missing adapt.js");
    let t_quality = body.find(r#"src="/assets/quality.js""#).expect("teacher.html missing quality.js");
    let t_reconnect = body.find(r#"src="/assets/reconnect.js""#).expect("teacher.html missing reconnect.js");
    let t_sessioncore = body.find(r#"src="/assets/session-core.js""#).expect("teacher.html missing session-core.js");
    let t_signalling = body.find(r#"src="/assets/signalling.js""#).expect("teacher.html missing signalling.js");
    assert!(t_adapt < t_quality);
    assert!(t_quality < t_reconnect);
    assert!(t_reconnect < t_sessioncore);
    assert!(t_sessioncore < t_signalling);

    let t_session_ui = body.find(r#"src="/assets/session-ui.js""#).expect("missing session-ui.js");
    assert!(t_session_ui < t_signalling);

    let t_chat_drawer = body.find(r#"src="/assets/chat-drawer.js""#).expect("missing chat-drawer.js");
    let t_panels = body.find(r#"src="/assets/session-panels.js""#).expect("missing session-panels.js");
    assert!(t_chat_drawer < t_session_ui);
    assert!(t_panels < t_session_ui);
    assert!(!body.contains(r#"id="chat-panel""#));

    app.shutdown().await;
}

